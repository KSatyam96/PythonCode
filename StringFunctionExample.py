print ("fruit[0:] >", fruit[0:])
print ("fruit[0:len(fruit)] >", fruit[0:len(fruit)])
# 
print ('\n')
print ("fruit[:-1] >", fruit[:-1])
print ("fruit[:len(fruit)-1] >", fruit[:len(fruit)-1])
