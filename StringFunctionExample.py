fruit = 'Mango'
print("fruit =",fruit)

#Get the length of the string
print("Lenght of fruit is ", len(fruit))

print ('\n')

# # Substring/Slicing/Part of string
print ("Print the character present at 0th index.")
print(fruit[0])

print ("Print the character present from 0th till 3rd index", fruit[0:4])

